// i. Grab element by its ID - getElementByID()
const title = document.getElementById('page-title');
console.log(title);

// ii. Grab elements by their class name - getElementByClassName()
const error = document.getElementsByClassName('error');
console.log(error);

// ii. Grab elements by their tag name - getElementByTagName()
const paras = document.getElementsByTagName('p');
console.log(paras);